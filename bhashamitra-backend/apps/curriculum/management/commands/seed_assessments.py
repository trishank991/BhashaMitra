class Command(BaseCommand):
    help = 'Seed assessments and questions for L1'

    def handle(self, *args, **options):
        self.stdout.write('Seeding Assessments...\n')

        assessment_created = 0
        assessment_updated = 0
        questions_created = 0

        with transaction.atomic():
            for assessment_data in ASSESSMENTS_DATA:
                assessment, created = Assessment.objects.update_or_create(
                    name=assessment_data['name'],
                    language=assessment_data['language'],
                    defaults=assessment_data
                )

                if created:
                    assessment_created += 1
                    self.stdout.write(f'  Created Assessment: {assessment.name}')
                else:
                    assessment_updated += 1
                    self.stdout.write(f'  Updated Assessment: {assessment.name}')

                # Seed questions for this assessment
                if assessment.name in QUESTIONS_DATA:
                    questions = QUESTIONS_DATA[assessment.name]

                    # Delete existing questions and recreate
                    AssessmentQuestion.objects.filter(assessment=assessment).delete()

                    for q_data in questions:
                        AssessmentQuestion.objects.create(
                            assessment=assessment,
                            **q_data
                        )
                        questions_created += 1

        self.stdout.write(self.style.SUCCESS(
            f'\nAssessments seeding complete!'
        ))
        self.stdout.write(f'  Assessments - Created: {assessment_created}, Updated: {assessment_updated}')
        self.stdout.write(f'  Questions Created: {questions_created}')
        self.stdout.write(f'  Total Assessments in DB: {Assessment.objects.count()}')
        self.stdout.write(f'  Total Questions in DB: {AssessmentQuestion.objects.count()}')
